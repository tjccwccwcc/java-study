import java.util.*;
public class ketang4_8 {
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);
        int a, b;
        while(sc.hasNextInt())//多组输入
        {
            a = sc.nextInt();
            b = sc.nextInt();
            System.out.println(a + b);
        }
    }
}